package oops;
public class Coordinates {
	private double x;
	public void setX(double x) {
		this.x = x;
	}
	public void setY(double y) {
		this.y = y;
	}
	private double y;
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public Coordinates(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	
}
